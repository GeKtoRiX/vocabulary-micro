#!/usr/bin/env bash
set -euo pipefail

SERVICES_DIR="${COMPOSE_SERVICES_DIR:-/app/backend/services}"
NODE_MODULES_DIR="${COMPOSE_SERVICES_NODE_MODULES_DIR:-$SERVICES_DIR/node_modules}"
LOCK_DIR="${COMPOSE_SERVICES_INSTALL_LOCK_DIR:-/app/.compose-services-install.lock}"
READY_FILE="${COMPOSE_SERVICES_INSTALL_READY_FILE:-/app/.compose-services-install.ready}"

if [ -x "$NODE_MODULES_DIR/.bin/tsx" ]; then
  exit 0
fi

while ! mkdir "$LOCK_DIR" 2>/dev/null; do
  if [ -x "$NODE_MODULES_DIR/.bin/tsx" ] && [ -f "$READY_FILE" ]; then
    exit 0
  fi
  sleep 1
done

cleanup() {
  rmdir "$LOCK_DIR" >/dev/null 2>&1 || true
}

trap cleanup EXIT

if [ ! -x "$NODE_MODULES_DIR/.bin/tsx" ]; then
  cd "$SERVICES_DIR"
  npm install
fi

touch "$READY_FILE"
